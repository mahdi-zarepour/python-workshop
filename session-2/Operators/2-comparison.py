"""
Comparison Operators
"""

less_than = 10 < 2
greater_than = 10 > 2

less_than_or_equal_to = 20 <= 1
greater_than_or_equal_to = 20 >= 1

equal_to = 1 == 1
not_equal_to = 1 != 0

print(less_than, greater_than, sep=' | ')
print(less_than_or_equal_to, greater_than_or_equal_to, sep=' | ')
print(equal_to, not_equal_to, sep=' | ')
print('A' == 'a')
